#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (!(*alst))
	{
		*alst = new;
		(*alst)->next = 0;
	}
	else
	{
		new->next = *alst;
		*alst = new;
	}
}
